#include "libft.h"

int		ft_putnstr(char *s, int n)
{
	int		i;

	if (s == NULL)
		return (0);
	i = 0;
	while (s[i] && i < n)
	{
		ft_putchar(s[i++]);
	}
	return (i);
}
